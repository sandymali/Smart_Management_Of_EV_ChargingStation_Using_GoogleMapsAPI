package com.bean;

public class Slot {
	
	private String slotname,slotaddress,slotarea,city,state,country;

	public String getSlotname() {
		return slotname;
	}

	public void setSlotname(String slotname) {
		this.slotname = slotname;
	}

	public String getSlotaddress() {
		return slotaddress;
	}

	public void setSlotaddress(String slotaddress) {
		this.slotaddress = slotaddress;
	}

	public String getSlotarea() {
		return slotarea;
	}

	public void setSlotarea(String slotarea) {
		this.slotarea = slotarea;
	}

	public String getCity() {
		return city;
	}

	public void setCity(String city) {
		this.city = city;
	}

	public String getState() {
		return state;
	}

	public void setState(String state) {
		this.state = state;
	}

	public String getCountry() {
		return country;
	}

	public void setCountry(String country) {
		this.country = country;
	}
	
	

}
